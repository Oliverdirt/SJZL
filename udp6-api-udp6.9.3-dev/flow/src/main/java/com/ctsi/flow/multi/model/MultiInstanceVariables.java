package com.ctsi.flow.multi.model;

/**
 * @author gyp
 */

public enum MultiInstanceVariables {
    /**
     * 多实例通过数
     */
    nrOfApprove,
    /**
     * 多实例拒绝数
     */
    nrOfReject,

    /**
     * 实例总数
     */
    nrOfInstances,

    /**
     * 已提交数
     */
    nrOfCompletedInstances,

    /**
     * 活动数
     */
    nrOfActiveInstances,

    /**
     * 循环数
     */
    loopCounter;

}
